const alunos = ['João', 'Vitor', 'Marina'];

// push adiciona no final da lista
alunos.push('Renan');

// adicionando dinamicamente escolhendo a posição
alunos[4] = 'Vinicius';
console.log(alunos);

// pop remove o ultimo item
console.log(alunos.pop());

console.log(alunos);

// shift remove o primeiro
console.log(alunos.shift());

console.log(alunos);
